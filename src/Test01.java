
public class Test01 {

	public static void main(String[] args) {

		System.out.println("Hello World!!");

		// int타입 변수 a 에 7을 대입
		int a = 7;
		// 실수형은 기본타입이 double이기때문에 float타입은 값뒤에 f를 붙임
		float b = 0.1f;
		// boolean타입은 true(참),false(거짓) 뿐이다
		boolean c = true;
		// char 문자 저장
		char d = 'a';
		// String(클래스)타입은 문자열 저장
		String e = "가나다";

		// 값 출력
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);

	}

}
